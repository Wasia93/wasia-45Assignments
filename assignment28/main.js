if (age_1 < 2) {
    console.log("Age is ".concat(age_1, ",The person is a baby."));
}
else if (age_1 == 2 && age_1 < 4) {
    console.log("Age is ".concat(age_1, ",The person is a toddler."));
}
else if (age_1 == 4 || age_1 < 13) {
    console.log("Age is ".concat(age_1, ",The person is a kid."));
}
else if (age_1 == 13 || age_1 < 20) {
    console.log("Age is ".concat(age_1, ",The person is a teenager."));
}
else if (age_1 == 20 || age_1 < 65) {
    console.log("Age is ".concat(age_1, ",The person is an adult."));
}
else {
    console.log("Age is ".concat(age_1, ",The person is an elder."));
}
